#!/usr/bin/env bash
set -euo pipefail

APP_ROOT="/opt/project-time-platform"
REPO_DIR="$APP_ROOT/app/project-time-platform"
ENV_FILE="$APP_ROOT/config/postgres.env"
MIGRATION_FILE="$REPO_DIR/database/migrations/011_remaining_psa_module_foundation.sql"
MIGRATION_ID="011_remaining_psa_module_foundation"

if [ ! -f "$ENV_FILE" ]; then
  echo "ERROR: Missing $ENV_FILE"
  exit 1
fi

if [ ! -f "$MIGRATION_FILE" ]; then
  echo "ERROR: Missing $MIGRATION_FILE"
  exit 1
fi

# shellcheck disable=SC1090
source "$ENV_FILE"

is_applied=$(PGPASSWORD="$PTP_DB_PASSWORD" psql \
  -h "$PTP_DB_HOST" \
  -p "$PTP_DB_PORT" \
  -U "$PTP_DB_USER" \
  -d "$PTP_DB_NAME" \
  -At \
  -c "SELECT CASE WHEN EXISTS (SELECT 1 FROM schema_migrations WHERE migration_id = '$MIGRATION_ID') THEN 'yes' ELSE 'no' END;")

if [ "$is_applied" = "yes" ]; then
  echo "==> Migration $MIGRATION_ID is already applied. Skipping schema apply step."
else
  echo "==> Applying migration: $MIGRATION_FILE"
  PGPASSWORD="$PTP_DB_PASSWORD" psql \
    -h "$PTP_DB_HOST" \
    -p "$PTP_DB_PORT" \
    -U "$PTP_DB_USER" \
    -d "$PTP_DB_NAME" \
    -v ON_ERROR_STOP=1 \
    -f "$MIGRATION_FILE"
fi

echo "==> Validating remaining PSA foundation tables"
PGPASSWORD="$PTP_DB_PASSWORD" psql \
  -h "$PTP_DB_HOST" \
  -p "$PTP_DB_PORT" \
  -U "$PTP_DB_USER" \
  -d "$PTP_DB_NAME" \
  -c "SELECT 'project_intake_requests' AS table_name, COUNT(*) FROM project_intake_requests UNION ALL SELECT 'project_templates', COUNT(*) FROM project_templates UNION ALL SELECT 'project_milestones', COUNT(*) FROM project_milestones UNION ALL SELECT 'project_risks', COUNT(*) FROM project_risks UNION ALL SELECT 'resource_capacity_plans', COUNT(*) FROM resource_capacity_plans UNION ALL SELECT 'expense_reports', COUNT(*) FROM expense_reports UNION ALL SELECT 'client_invoices', COUNT(*) FROM client_invoices UNION ALL SELECT 'reporting_snapshots', COUNT(*) FROM reporting_snapshots ORDER BY table_name;"

echo "==> Migration 011 validation complete"
